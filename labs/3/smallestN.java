public class smallestN {
	public static void main (String args[]){
		//find the smallest N1
		double n = 0; // the smallest value of n
		while (true){
			n++;
			if ((n * n ) >= 12000){
				break;
		
			}
		}
		System.out.println("The smallest value is:" + n);
	}

}
